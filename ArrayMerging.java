package com.kn.arrays;

public class ArrayMerging {
	public void MergeOfArrays(int[]arr,int[]brr) {
		System.out.println("******");
//		Array Traversing
		for(int i=0;i<arr.length;i++)
		{
			{
				System.out.println(arr[i]);
			}
		}
		for(int i=0;i<brr.length;i++)
		{
			{
				System.out.println(brr[i]);
			}
		}
	}
}
